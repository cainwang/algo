    public ListNode sortList(ListNode head) {
        if (head == null) {
            return head;
        }

        return doSort(head)[0];
    }

    private ListNode[] doSort(ListNode head) {
        ListNode pivot = head;
        ListNode pivotEnd = head;
        int pivotValue = pivot.val;

        ListNode next = pivot.next;
        pivot.next = null;

        if (next == null) {
            return new ListNode[] { head, head };
        }

        ListNode less = null, lessTail = null, more = null, moreTail = null;

        while (next != null) {
            int nextVal = next.val;

            if (nextVal < pivotValue) {
                if (less == null) {
                    less = next;
                    lessTail = next;
                } else {
                    lessTail.next = next;
                    lessTail = next;
                }
            } else if (nextVal > pivotValue ) {
                if (more == null) {
                    more = next;
                    moreTail = next;
                } else {
                    moreTail.next = next;
                    moreTail = next;
                }
            } else {
                pivotEnd.next = next;
                pivotEnd = next;
            }

            next = next.next;
        }

        if (lessTail != null) {
            lessTail.next = null;
        }
        if (moreTail != null) {
            moreTail.next = null;
        }
        pivotEnd.next = null;

        ListNode[] lessResults = null, moreResults = null;
        if (less != null) {
            lessResults = doSort(less);
        }
        if (more != null) {
            moreResults = doSort(more);
        }

        ListNode resultHead, resultEnd;
        if (lessResults != null && moreResults != null) {
            resultHead = lessResults[0];
            lessResults[1].next = pivot;
            pivotEnd.next = moreResults[0];
            resultEnd = moreResults[1];
        } else if (lessResults == null && moreResults == null) {
            resultHead = pivot;
            resultEnd = pivotEnd;
        } else if (lessResults == null) {
            resultHead = pivot;
            pivotEnd.next = moreResults[0];
            resultEnd = moreResults[1];
        } else {
            resultHead = lessResults[0];
            lessResults[1].next = pivot;
            resultEnd = pivotEnd;
        }

        return new ListNode[] { resultHead, resultEnd };
    }
